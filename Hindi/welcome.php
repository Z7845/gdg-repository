                - फसलों को बदलने से फसल-विशिष्ट कीटों और रोगाणुओं के जीवन चक्र को बाधित किया जाता है, जिससे उनकी जनसंख्या कम होती है और रासायनिक कीटनाशकों की आवश्यकता कम होती है।
