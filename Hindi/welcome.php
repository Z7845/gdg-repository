        <script>
            function setReminder() {
                const taskInput = document.getElementById('task').value;
                const timeInput = document.getElementById('time').value;
                const alarmSound = document.getElementById('alarmSound');
    
                if (!taskInput || !timeInput) {
                    alert('कृपया कार्य विवरण और समय दोनों दर्ज करें।');
                    return;
                }
    
                const taskTime = new Date(timeInput).getTime();
                const currentTime = new Date().getTime();
                const timeDifference = taskTime - currentTime;
    
                if (timeDifference <= 0) {
                    alert('कृपया भविष्य का समय दर्ज करें।');
                    return;
                }
    
                setTimeout(() => {
                    alarmSound.play();
                    alert(`शुरू करने का समय: ${taskInput}`);
                }, timeDifference);
            }
        </script>
